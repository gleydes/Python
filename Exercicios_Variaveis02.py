# Define as variáveis
x = 12
y = 8
z = 15

# Calcula os valores das variáveis A, B, C, D, E e F
A = (35*y + 70*z - 8*x) / (9*z - x) * 7*y
B = (40*y + 80*z - 9*x) / (10*x - y) + 8*z
C = (25*y + 50*z - 6*x) / (7*z - 8*x) + 5*y
D = A + B
E = D / (3*z + 5*y)
F = D*E - (-9*x + y**2)

# Apresenta os valores das variáveis
print("A =", A)
print("B =", B)
print("C =", C)
print("D =", D)
print("E =", E)
print("F =", F)